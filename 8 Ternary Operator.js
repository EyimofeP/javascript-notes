// SHortcut for If- Else Statement 
// Format-- condition ? codeIfTrue : codeIfFalse

// Normal if Else Statement
let age = 21

if (age < 18) {
    let message = "You are a minor!"
} else {
    message = "You're an adult"
}

// Ternary Operator
message = age < 18 ? "You are a minor!" : "You're an adult"